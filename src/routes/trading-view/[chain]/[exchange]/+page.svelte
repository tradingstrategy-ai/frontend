<script lang="ts">
	import type { PageData } from './$types';
	import type { ComponentEvents } from 'svelte';
	import { page } from '$app/stores';
	import { goto } from '$app/navigation';
	import { getPairsClient } from '$lib/explorer/pair-client';
	import { parseExchangeName } from '$lib/helpers/exchange';
	import { formatUrlAsDomain } from '$lib/helpers/formatters';
	import { AlertItem, AlertList, Button, EntityPageHeader, SummaryBox } from '$lib/components';
	import Breadcrumbs from '$lib/breadcrumb/Breadcrumbs.svelte';
	import PairsTable from '$lib/explorer/PairsTable.svelte';
	import InfoTable from './InfoTable.svelte';
	import InfoSummary from './InfoSummary.svelte';

	export let data: PageData;

	$: nameDetails = parseExchangeName(data.human_readable_name);

	// FIXME: it is preferable to use `liquidity_type === 'xyliquidity'` for below conditional,
	// but this is not currently available from `exchange-details` endpoint.
	// See: https://github.com/tradingstrategy-ai/backend/issues/110
	let hiddenColumns: string[];
	$: if (data.exchange_type === 'uniswap_v3') {
		hiddenColumns = ['exchange_name', 'liquidity', 'liquidity_change_24h'];
	} else {
		hiddenColumns = ['exchange_name'];
	}

	const pairsClient = getPairsClient(fetch);

	$: $page.route.id?.endsWith('[exchange]') &&
		pairsClient.update({
			chain_slugs: data.chain_slug,
			exchange_slugs: data.exchange_slug,
			...Object.fromEntries($page.url.searchParams.entries())
		});

	async function handlePairsChange({ detail }: ComponentEvents<PairsTable>['change']) {
		await goto('?' + new URLSearchParams(detail.params), { noScroll: true });
		detail.scrollToTop();
	}
</script>

<svelte:head>
	<title>
		{data.human_readable_name} on {data.chain_name}
	</title>
	<meta
		name="description"
		content={`Decentralise exchange ${data.human_readable_name} on ${data.chain_name} blockchain`}
	/>
</svelte:head>

<Breadcrumbs labels={{ [data.exchange_slug]: data.human_readable_name }} />

<main>
	<EntityPageHeader name={data.human_readable_name} slug={data.exchange_slug} homepage={data.homepage}>
		<span slot="subtitle">on {data.chain_name}</span>
		<Button slot="actions">Visit {formatUrlAsDomain(data.homepage)}</Button>
		<svelte:fragment slot="details-left">
			<SummaryBox title="Blockchain details">
				<table>
					<tbody>
						<colgroup>
							<col />
						</colgroup>
						{#if data.homepage}
							<tr>
								<th scope="row">Volume 30d</th>
								<td>Needs data</td>
							</tr>
						{/if}
						{#if data.homepage}
							<tr>
								<th scope="row">Volume all-time</th>
								<td>Needs data</td>
							</tr>
						{/if}
						{#if data.homepage}
							<tr>
								<th scope="row">Trading pairs</th>
								<td>Needs data</td>
							</tr>
						{/if}
						{#if data.homepage}
							<tr>
								<th scope="row">Tracked trading pairs</th>
								<td>Needs data</td>
							</tr>
						{/if}
						{#if data.homepage}
							<tr>
								<th scope="row">Version</th>
								<td>Needs data</td>
							</tr>
						{/if}
						{#if data.homepage}
							<tr>
								<th scope="row">Type</th>
								<td>Needs data</td>
							</tr>
						{/if}
						{#if data.homepage}
							<tr>
								<th scope="row">Internal ID</th>
								<td>Needs data</td>
							</tr>
						{/if}
					</tbody>
				</table>
			</SummaryBox>
		</svelte:fragment>

		<SummaryBox slot="details-right" title="Description">
			<InfoSummary details={data} />
		</SummaryBox>

		<SummaryBox class="actions-box" slot="bottom" title="Actions">
			<Button label="Visit {nameDetails.name}" href={data.homepage} />
			<Button label="View {nameDetails.name} on blockchain explorer" href={data.blockchain_explorer_link} />
			<Button label="Download as Excel" href="/trading-view/{data.chain_slug}/{data.exchange_slug}/export-data" />
		</SummaryBox>
	</EntityPageHeader>

	<section class="ds-container info" data-testid="exchange-info">
		<AlertList status="warning">
			<AlertItem title="Uniswap v3 beta" displayWhen={data.exchange_type === 'uniswap_v3'}>
				We are in the process of integrating Uniswap V3 data. This page is available as a beta preview, but please note
				that the data for this exchange is currently incomplete.
			</AlertItem>

			<AlertItem title="Incompatible exchange" displayWhen={data.exchange_type === 'uniswap_v2_incompatible'}>
				{data.human_readable_name} is not fully compatible with Uniswap v2 protocols. Price, volume and liquidity data for
				this exchange may be inaccurate.
			</AlertItem>
		</AlertList>
	</section>

	<section class="ds-container trading-pairs">
		<h2>Trading Pairs</h2>

		{#if !$pairsClient.error}
			<PairsTable {...$pairsClient} {hiddenColumns} on:change={handlePairsChange} />
		{:else}
			<AlertList>
				<AlertItem>
					An error occurred loading the pairs data. Check the URL parameters for errors and try reloading the page.
				</AlertItem>
			</AlertList>
		{/if}
	</section>

	<aside class="ds-container">
		<p>
			Not all trading pairs are being displayed or included in volume calculations.
			<a href="https://tradingstrategy.ai/docs/programming/market-data/tracking.html">See inclusion criteria</a>.
		</p>
	</aside>
</main>

